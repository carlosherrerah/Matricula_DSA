package u1;

public class Persona {
    int id;
    String nombre;
    char sexo;
    private int edad;
    public int getId() {
        return id;
    }
    public void setId(int x) {
        id = x;
    }
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public char getSexo() {
        return sexo;
    }
    public void setSexo(char sexo) {
        this.sexo = sexo;
    }
    public int getEdad() {
        return edad;
    }
    public void setEdad(int edad) {
        this.edad = edad;
    } 

    

}
